// Array1 containing numbers using Array Literal
const array1 = [1, 2, 3, 4, 5];

// Array2 containing strings using Array Constructor
const array2 = new Array('apple', 'banana', 'orange');

// Array3 mixed with numbers and strings using Array.from()
const array3 = Array.from([1, 'hello', 3, 'world', 5]);

// Console.log all the arrays
console.log(array1);
console.log(array2);
console.log(array3);
